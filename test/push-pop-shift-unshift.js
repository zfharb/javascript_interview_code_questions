
let cats = ['Bob', 'Willy', 'Mini'];

cats.pop(); // ['Bob', 'Willy']

console.log(cats);


cats = ['Bob'];

cats.push('Willy'); // ['Bob', 'Willy']

cats.push('Puff', 'George'); 

console.log(cats);


cats = ['Bob', 'Willy', 'Mini'];

cats.shift()

console.log(cats);

cats = ['Bob'];

cats.unshift('Willy'); // ['Willy', 'Bob']

cats.unshift('Puff', 'George')

console.log(cats);
